<h1>¿Qué es Voto Colombia 2018?</h1>
<p>Voto Colombia 2018 surgió de un interés individual por conocer las intenciones de voto con miras a las 
elecciones presidenciales del 2018 en Colombia.</p>
<p>Esta es una iniciativa que no cuenta ni con nuestro propio apoyo. Por ello no le hacemos barra a ningún 
candidato ni pertenecemos a ningún movimiento político o grupo de pensamiento o ideológico, no recibimos 
aportes de ningún conglomerado económico ni tienda barrio, no estamos vinculados a ninguna organización 
gubernamental o no gubernamental. No somos enviados de ninguna potencia extranjera ni tenemos vínculos con 
las mafias japonesas o italianas.</p>
<p>De igual manera, no queremos beneficiarnos con los datos obtenidos de los participantes, de ahí la 
anonimidad de las respuestas ante este ejercicio. No estamos regulados ni vigilados por la Registraduría 
Nacional del Estado Civil, la Superintendencia de Puertos y Transporte ni por el Comité de  Convivencia de 
nuestro conjunto.</p>
<p>Tanto las tablas de resultados
  (<a href="http://morra.co/presidencia2018/app/data/data.csv" download="ResultadosVotoPresidenciaColombia2018.csv">Resultados Presidencia Colombia 2018</a>) 
  como el mismísimo código fuente de nuestra página de respuestas 
  (<a href="https://github.com/manuelhe/bogotamayorpoll2015" target="_blank">Repositorio en Github</a>) 
  se encuentran disponibles para su escrutinio y comentarios por parte del público (MIT Licensed).</p>
<p>Este, finalmente, es sólo un ejercicio académico, sin la menor validez y rigurosidad que pueden tener los 
grandes encuestadores. No pretendemos dar un resultado ni especular  sobre lo inespeculable. Nuestro 
presupuesto inicial es de $0.00 pesos colombianos, lo cual nos limita ostensiblemente para realizar una mejor 
tarea.</p>
<p>Y ¡muchas gracias por participar!</p>
