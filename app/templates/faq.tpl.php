<h1>Preguntas Frecuentes</h1>

<dl>
  <dt>¿Por qué el candidato <em>x</em> tiene tantos/tan pocos votos en los resultados?</dt>
  <dd>Es posible que los votos por el candidato de su interés no se vean reflejados de la manera en que 
    usted podría esperar. Esto se puede deber al mecanismo de propagación de este ejercicio y a su área de influencia:
    debido a su connotación netamente viral, no podemos asegurar que el muestreo sea equitativo y representativo.
    Por tal razón, no podemos asegurar que los participantes anónimos en la encuesta reflejen la realidad de toda una nación.
  </dd>
  
  <dt>¿Es verdad que esta encuesta es de <em>x</em> candidato?</dt>
  <dd>Este frecuente interrogante lo vamos a responder de manera muy sucinta: NO.</dd>
  
  <dt>¿Por qué preguntan hasta el tipo de sangre?</dt>
  <dd>Varias de las preguntas que realizamos en la encuesta nos permiter tener una caracterización más detallada de los 
    grupos de votantes. De esta manera, es posible, a pesar del posible sesgo en los resultados, brindar un análisis más
    certero de la realidad electoral. Sobre la pregunta del tipo de sangre, en principio fue más una inquietud ingenua pero
    ha demostrado su valía al ser un indicador efectivo de que, en general, los votos son, en su gran mayoría, reales.
    Esto lo podemos afirmar debido a las conocidas distribuciones estadísticas de los grupos sanguíneos de manera universal.
    <br><br>
    También se dice que a algunos políticos no les corre la sangre por las venas y queríamos verificar si sus electores
    compartían dicha característica.
  </dd>

  <dt>¿Cúal es el origen de los recursos/capital/presupuesto/aportes de esta encuesta?</dt>
  <dd>Este es un ejercicio surgido de una inquietud ciudadana e individual. Nuestro presupuesto inicial fue de $0.00 pesos
    colombianos, el cual hemos mantenido hasta el momento. No recibimos aportes, ayudas, recursos, empréstitos, donaciones
    ni en efectivo ni en especie, subsidios, coimas, mermelada (tenemos problemas de azucar), inversiones, o cualquier otro 
    tipo de inyección de capital en este proyecto.
  </dd>

  <dt>¿Hacen parte de algún tipo de organización/entidad/partido?</dt>
  <dd>Sin que nos cansemos de repetirlo, no hacemos parte de ninguna organización gubernamental o no gubernamental, 
    movimiento político, grupo de pensamiento, medio de comunicación, entidad ó secta religiosa, equipo deportivo, 
    barra brava, conglomerado industrial ó tienda de barrio, potencia extranjera, movimiento subversivo o criminal,
    destacamento militar, club de otakus, trío de mariachis, consejo gremial, pandilla de barrio, grupo de oración ó
    misión carismática, comité de convivencia, junta comunal, manada de desadaptados ó cualquier otro tipo de organización
    de la cual hagan parte dos o más personas.
  </dd>

  <dt>¿Por qué afirman ser una encuesta transparente?</dt>
  <dd>Nuestro argumento fundamental para realizar esta afirmación es que tanto los datos 
  (<a href="http://morra.co/presidencia2018/data/data.csv" target="_blank">http://morra.co/presidencia2018/data/data.csv</a>) 
  cómo el código fuente 
  (<a href="https://github.com/manuelhe/bogotamayorpoll2015" target="_blank">https://github.com/manuelhe/bogotamayorpoll2015</a>) 
  de este sitio web que presenta las respuestas se encuentran totalmente disponibles para el escrutinio, análisis, comentarios,
  reproches, quejas, suplicas, interpelaciones, diátrabas y cualquier otro tipo de dilucidaciones por parte del público
  en general. De esta manera aseguramos que las gráficas y resultados aquí contenidos son un fiel reflejo de los votos
  de cada uno de los participantes.
  </dd>

  <dt>¿Cada cuanto actualizan los resultados en la página?</dt>
  <dd>Literalmente, cada vez que podemos. Este es, en estos momentos, un proceso manual y no tenemos dentro de nuestros
  planes a corto plazo modificar tal procedimiento.
  </dd>

  <dt>¿Por qué me preguntan por mi salario? ¿Me van a robar mis datos?</dt>
  <dd>Otra de nuestras premisas fundamentales es la participación totalmente anónima en este ejercicio. Por ello, las
  preguntas que realizamos sólo sirven para realizar una caracterización de los grupos de votantes y sus preferencias
  electorales. Estamos seguros y totalmente confiados que no es posible la identificación individual y manipulación de
  los datos obtenidos para otros fines diferentes al de esta encuesta
  </dd>

  <dt>Esta encuesta está mal, hasta mi perro puede votar ¿lo sabían?</dt>
  <dd>
    Lo sabemos. Un primer debate que tuvimos fue en torno a la individualización de los votantes para garantizar un menor
    margen de error a los resultados ó la anonimidad de sus participantes. Claramente ganó este último precepto. No tenemos
    el más mínimo interés en sacar provecho alguno de los datos recopilados; al mismo tiempo, deseamos que sea un ejercicio
    abierto en el cual los datos puedan ser de dominio público. Por otro lado, no queremos lidiar con procesos legales
    por el uso indebido de datos personales (más información aquí: 
    <a href="http://www.sic.gov.co/sobre-la-proteccion-de-datos-personales" target="_blank">Sobre la protección de datos personales</a>).
  </dd>

  <dt>Y ¿por qué no implementaron alguna medida menos invasiva, como el registro por IP?</dt>
  <dd>Medidas como esa (ó el uso de cookies u otro mecanismo de registro similar) pueden ser contrarrestadas o 
  contraproducentes para este ejercicio. En el caso del registro por dirección IP, muchas personas comparten este dato,
  lo cual limitaría su participación. El uso de cookies para limitar participaciones fácilmente puede ser evadido.
  Por último, la plataforma seleccionada para el formulario de la encuesta fue 
  <a href="https://www.google.com/forms/about/" target="_blank">Google Forms</a> simplemente por rapidez y simplicidad,
  esto nos limita enormemente a la hora de implementar algún tipo de medida restrictiva que no sea la exigencia de una 
  cuenta en dicha compañía por parte de los participantes.
  </dd>

  <dt>¿Por qué sus resultados no coinciden con los de las grandes encuestadoras?</dt>
  <dd>Primero que todo, no somo una gran encuestadora. De hecho, no somos ni encuestadora ni somos grandes.
  Los resultados aquí presentados NO son nuestros resultados. No nos apropiamos ni damos la cara por los mismos. Sólo 
  somos una plataforma para la presentación de los datos recopilados. El análisis, escrutinio y crítica de los mismos 
  está en manos de los visitantes y público de nuestra página.
  </dd>

  <dt>Si estaban tan preguntones ¿por qué no preguntaron por nivel de estudios y profesión?</dt>
  <dd>Esta fue una inmensa omisión la cual, hasta el día de hoy, nos persigue hasta en nuestros sueños más lujuriosos.
  Realmente nos invade la tristeza de haber pasado por alto estos dos puntos en nuestra encuesta. Ese es el
  problema de este tipo de proyecto con cero planeación, realizados por un grupo de inexpertos como lo somos nosotros.
  Esperamos nos perdonen esta vez, de seguro sería muy interesante saber por quien votarían las historiadoras, 
  santandereanas, PhDs, adventistas, AB+ entre los 34 y 40 años que viven en estrato 4.
  </dd>

  <dt>¿Qué trampa van a hacer con los resultados obtenidos?</dt>
  <dd> Se encuentra en la individualidad de los participantes de este ejercicio el determinar su accionar frente a una 
  posición de vulnerabilidad de las leyes aplicables a los procesos electorales de nuestra nación. Nosotros sólo somos 
  un medio que garantiza la transparencia de los resultados obtenidos. De ninguna manera intentamos ser "influencers" 
  políticos, no pretendemos imponer una única verdad, ni dar respuesta a algo que no es más que una vulgar especulación. 
  Eso sí, estamos en total concordancia con la preocupación sobre las futuras elecciones: desemos fervorosamente que no
  se produzcan ningún tipo de delitos electorales ó trampa.
  </dd>

  <dt>Bueno y ¿quién es Nyarlathotep y cómo es eso que consumirá las almas que contesten varias veces?</dt>
  <dd>Aquí una referencia bastante clara sobre quién es Nyarlathotep: 
  <a href="http://es.hplovecraft.wikia.com/wiki/Nyarlathotep" target="_blank">http://es.hplovecraft.wikia.com/wiki/Nyarlathotep</a>.
  Es nuestro dios externo favorito y es a lo único que podemos abogar para garantizar la "veracidad" en los
  resultados de este ejercicio.
  </dd>

  <dt>Me parece una burla de encuesta para un tema tan serio ¿lo sabían?</dt>
  <dd>No hemos pedido que nos tomen en serio (ni nosotros mismo lo hacemos). No tenemos ni asumimos o pretendemos tener 
  el menor ápice de credibilidad, ni nos pagan por ello (bueno, no nos pagan nada, de hecho). Tampoco somos instrumento 
  de información, más bien al contrario; no somos expendedores de noticias, o plataforma ideológica o de comunicaciones, 
  este es un esfuerzo, porque realmente nos cuesta, totalmente individual y sin la menor argumentación teórica ó moral.
  De ninguna manera queremos ayudar a los votantes, estamos convencidos que no somos de gran ayuda y hay algunos que ya 
  no tienen salvación.
  <br><br>
  Sabemos de la magnitud y seriedad del tema en cuestión y compartimos la misma preocupación en la mismas proporciones
  en las que Aida Merlano se afanó en respetar la democracia en las pasadas elecciones legislativas.
  </dd>

  <dt>¿Quiénes son ustedes en realidad?</dt>
  <dd>Aunque lo pareciera, no tenemos la intención de pasar totalmente por el anonimato, con pasamontañas y voces
  distorsionadas si nos llaman. Pero les dejamos a ustedes la tarea de investigarlo. Y, realmente, es muy fácil
  dar con nosotros.
  </dd>

</dl>

